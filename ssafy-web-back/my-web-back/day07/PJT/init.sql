DROP DATABASE IF EXISTS pjt;
CREATE DATABASE pjt DEFAULT CHARACTER SET utf8mb4;

USE pjt;

CREATE TABLE fortune (
    date VARCHAR(20) NOT NULL PRIMARY KEY,
    today_luck VARCHAR(50) NOT NULL
);
CREATE TABLE list (
    id INT AUTO_INCREMENT,
    date VARCHAR(20) NOT NULL,
    content VARCHAR(50) NOT NULL,
    semicontent VARCHAR(50) NOT NULL,
    PRIMARY KEY(id),
    CONSTRAINT date FOREIGN KEY (date) REFERENCES fortune(date)
);

